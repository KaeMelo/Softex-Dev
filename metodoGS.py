class Pessoa: 
     def __init__(self, nome, corCabelo): 
         self.nome = nome 
         self.corCabelo = corCabelo
  
     def getNome(self): 
         return self.nome 
  
     def setNome(self, nome): 
         self.nome = nome 
  
     def getcorCabelo(self, corCabelo):
         self.corCabelo = corCabelo
  
     def setcorCabelo(self, corCabelo): 
         self.corCabelo = corCabelo
